<template>
    <div class="toasts">
    <UiToast
      v-for="toast in toasts"
      :key="toast.id"
      :type="toast.type"
      :message="toast.message"
      :time="toast.time"
      @destroy="destroyHandler(toast)"
    />
  </div>

</template>
<script>
import UiToast from './UiToast.vue';

export default {
  name:'TheToastList',

  components: { UiToast },

  emits: ['delete'],

  props: {
    toasts: {
      requierd: true,
      type: Array
    }
  },

  methods: {
    destroyHandler(toast) {
      this.$emit('delete', toast)
    }
  }
}
</script>
